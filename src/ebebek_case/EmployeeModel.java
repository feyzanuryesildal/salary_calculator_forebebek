package ebebek_case;
public class EmployeeModel {
    private String _name;
    private double _salary;
    private double _workHours;
    private int _hireYear;

    public String get_name() {
        return _name;
    }
    public void set_name(String _name) {
        this._name = _name;
    }
    public double get_salary() {
        return _salary;
    }
    public void set_salary(double _salary) {
        this._salary = _salary;
    }
    public double get_workHours() {
        return _workHours;
    }
    public void set_workHours(double _workHours) {
        this._workHours = _workHours;
    }
    public int get_hireYear() {
        return _hireYear;
    }
    public void set_hireYear(int _hireYear) {
        this._hireYear = _hireYear;
    }

}
